package com.railway.model;

public class TSrelation {
	private String Tid;
	private String sid;
	private String Rarrivetime;
	private String Rstarttime;

	public String getTid() {
		return Tid;
	}

	public void setTid(String tid) {
		Tid = tid;
	}

	public String getSid() {
		return sid;
	}

	public void setSid(String sid) {
		this.sid = sid;
	}

	public String getRarrivetime() {
		return Rarrivetime;
	}

	public void setRarrivetime(String rarrivetime) {
		Rarrivetime = rarrivetime;
	}

	public String getRstarttime() {
		return Rstarttime;
	}

	public void setRstarttime(String rstarttime) {
		Rstarttime = rstarttime;
	}
}
